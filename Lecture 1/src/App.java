
// class: The outer container for code.
// Its name must match the file name
public class App {
    // Main Method: The entry point of a Java program.
    public static void main(String[] args) throws Exception {
        // Statements: A single line of code that performs a specific task.
        System.out.println("Hello, World!");
        /*
         * System.out.println:
         * Used to print output to the console.
         * Examples:
         * System.out.println("Hello, World!"); → Prints text.
         * System.out.println(); → Prints a blank line.
         */
        System.out.println("\t Hi"); // \t is used for tab space
        System.out.println("\n Hello"); // \n is used for new line
        System.out.println("\"hello\""); // \" is used for double quotes
        System.out.println("\\hello\\"); // \\ is used for backslash
        
    }
}
